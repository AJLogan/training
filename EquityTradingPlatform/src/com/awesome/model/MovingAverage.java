package com.awesome.model;

public class MovingAverage {
	private double sma;
	private double lma;

	/**
	 * @return the sma
	 */
	public double getSma() {
		return sma;
	}

	/**
	 * @param sma
	 *            the sma to set
	 */
	public void setSma(double sma) {
		this.sma = sma;
	}

	/**
	 * @return the lma
	 */
	public double getLma() {
		return lma;
	}

	/**
	 * @param lma
	 *            the lma to set
	 */
	public void setLma(double lma) {
		this.lma = lma;
	}
}
